"""
Compile elections/*.txt tab files into AED_ELECTIONS for the frontend.
Run from repo root: python3 scripts/build_maps.py
"""

import json
import os
from pathlib import Path


def repo_root_here() -> Path:
    """Parent of scripts/ when this module lives under scripts/."""
    return Path(__file__).resolve().parent.parent


def update_all(repo_root: Path | None = None) -> dict:
    """Compile election TXTs → JSON/embed JS; refresh year selects in HTML pages."""
    if repo_root is None:
        repo_root = repo_root_here()
    data = compile_all(repo_root / "elections", repo_root)
    write_election_assets(data, repo_root)
    return data


def compile_all(elections_dir: Path, repo_root: Path) -> dict:
    """elections_dir contains subfolders US_president_state, etc."""
    out = {}
    for folder in elections_dir.iterdir():
        if not folder.is_dir():
            continue
        name = folder.name
        out[name] = compile_folder(folder, name[:3] == "US_", repo_root)
    return out


def compile_folder(folder: Path, state: bool, repo_root: Path) -> dict:
    out = {}
    years = []
    for path in sorted(folder.iterdir()):
        if path.is_file() and path.suffix == ".txt":
            year_key = path.stem
            out[year_key] = parse(path, state)
            years.append(year_key)
    html_name = folder.name + ".html"
    update_html(repo_root / html_name, years)
    return out


def parse(filename: Path, state: bool = False) -> dict:
    from utility import get_state_abbr, get_party_lst

    results = {}
    parties = []
    colors = []
    with filename.open(encoding="utf-8") as fh:
        for line in fh:
            lst = line.strip().split("\t")
            if lst[0] == "_PARTIES":
                parties = get_party_lst(lst[1:])
            elif lst[0] == "_COLORS":
                colors = lst[1:]
            else:
                results[lst[0]] = lst[1:]
    out = {}
    color_dict = {}
    for i in range(len(colors)):
        color_dict[parties[i]] = colors[i]
    for div in results:
        temp = {}
        tot = 0
        for i in range(len(parties)):
            if i >= len(results[div]):
                if i == len(parties) - 1:
                    temp[parties[i]] = tot
                else:
                    temp[parties[i]] = 0
            else:
                if results[div][i] == "":
                    temp[parties[i]] = 0
                else:
                    v = int(results[div][i])
                    temp[parties[i]] = v
                    tot += v
        out[get_state_abbr(div, state)] = temp
    out["_COLORS"] = color_dict
    return out


def write_election_assets(data: dict, repo_root: Path) -> None:
    """Write canonical JSON plus a synchronous script tag payload for browsers."""
    out_dir = repo_root / "data"
    out_dir.mkdir(parents=True, exist_ok=True)
    js_path = out_dir / "elections.embed.js"
    json_path = out_dir / "elections.json"
    dumped = json.dumps(data, separators=(",", ":"), ensure_ascii=False)
    json_path.write_text(dumped, encoding="utf-8")
    js_blob = (
        "/* Generated by scripts/build_maps.py — do not edit */\n"
        "\"use strict\";\nvar AED_ELECTIONS = "
        + dumped
        + ";\n"
    )
    js_path.write_text(js_blob, encoding="utf-8")


def update_html(html_path: Path, years: list[str]) -> None:
    """Replace option list inside the #election-year select."""
    import re

    years = sorted(years, key=lambda x: int(x), reverse=True)
    if not html_path.exists():
        return
    options = "".join(f'\t\t<option value="{y}">{y}</option>\n' for y in years)
    text = html_path.read_text(encoding="utf-8")
    pattern = re.compile(
        r'(<select\b[^>]*\bid="election-year"[^>]*>)([\s\S]*?)(</select>)',
        re.IGNORECASE,
    )
    replacement = lambda m: m.group(1) + "\n" + options + m.group(3)
    new_text, count = pattern.subn(replacement, text, count=1)
    if count != 1:
        return
    html_path.write_text(new_text, encoding="utf-8")


if __name__ == "__main__":
    update_all()
